package org.test;

public class CharacterofString {
public static void main(String[] args) {
	String a="GreenTechnology";
	String b="SeleniumAutomationTool";
	String c="velmurugan";
	String d="j a v a p r o g r a m";
	String e="9095484678";
	System.out.println("Print the Character h in String a :"+a.charAt(8));
	System.out.println("Print the Character o in String b :"+b.charAt(11));
	System.out.println("Print the Character u in String c :"+c.charAt(4));
	System.out.println("Print the Character p in String d :"+d.charAt(8));
	System.out.println("Print the Character 7 in String e :"+e.charAt(8));
}
}
