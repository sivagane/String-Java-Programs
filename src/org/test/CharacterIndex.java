package org.test;

public class CharacterIndex {
public static void main(String[] args) {
	String a="GreenTechnology";
	String b="SeleniumAutomationtool";
	String c="Velmurugan";
	String d="j a v a p r o g r a m";
	String e="9095484678";
	System.out.println("The Last Index of String a is "+a.lastIndexOf('o'));
	System.out.println("The Index of String b is "+b.indexOf('o'));
	System.out.println("The Index of String c is "+c.indexOf('n'));
	System.out.println("The Index of String d is "+d.indexOf(' '));
	System.out.println("The Index of String e is "+e.indexOf(8));
}
}
